package co.uniquindio.fundamenos.biblioteca;

public class Biblioteca {
	/*
	 * atributos de la clase Biblioteca
	 */
	private int cantLibros;
	private int cantEstanterias;
	private int cantLectores;
	
	/*
	 * @param  es la cantidad de libros
	 */
	public void setCantLibros(int cantLibros) {
		this.cantLibros= cantLibros;
	}
	
	/*
	 * @param es la cantidad de estanterias
	 */
	public void setCantEstanterias(int cantEstanterias) {
		this.cantEstanterias=cantEstanterias;
	}

	/*
	 * @param es la cantidad de lectores
	 */
	public void setCantLectores(int cantLectores) {
		this.cantLectores=cantLectores;
	}

}



