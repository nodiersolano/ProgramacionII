package co.uniquindio.fundamenos.biblioteca;

public class Estanteria {
	/*
	 * atributos de la clase Estanteria
	 */
	private double tamaño;
	private String color;
	private int cantLibros;
	
	/*
	 * @param es el tammaño de la estanteria
	 */
	public void setTamaño(int tamaño) {
		this.tamaño= tamaño;
	}
	
	/*
	 * @param es el color de la estanteria
	 */
	public void setColor(String color) {
		this.color=color;
	}

	/*
	 * @param es la cantidad de libros que tiene la estanteria
	 */
	public void setCantLibros(int cantLibros) {
		this.cantLibros=cantLibros;
	}

}




